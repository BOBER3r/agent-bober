// ── Determinism ────────────────────────────────────────────────────

describe("OnboardingComposer.render() — determinism", () => {
  it("renders byte-identical output on two consecutive calls", () => {
    const composer = new OnboardingComposer();
    // Use a fixed date to avoid timestamp differences
    const fixedDate = new Date("2026-05-24T12:00:00.000Z");
    const origDate = global.Date;
    global.Date = class extends origDate {
      constructor(...args: ConstructorParameters<typeof origDate>) {
        if (args.length === 0) {
          super(fixedDate.getTime());
        } else {
          // @ts-expect-error spread args
          super(...args);
        }
      }
      toISOString(): string {
        return fixedDate.toISOString();
      }
    } as unknown as typeof Date;

    try {
      const first = composer.render(POPULATED_INPUTS);
      const second = composer.render(POPULATED_INPUTS);

      expect(first.readme).toBe(second.readme);
      expect(first.architectureOverview).toBe(second.architectureOverview);
      expect(first.hotspots).toBe(second.hotspots);
      expect(first.knowledgeGaps).toBe(second.knowledgeGaps);
      expect(first.communities).toBe(second.communities);
    } finally {
      global.Date = origDate;
    }
  });
});

// ── Sort verification ──────────────────────────────────────────────

describe("OnboardingComposer.render() — sort verification", () => {
  const composer = new OnboardingComposer();

  it("architectureOverview modules are alphabetical", () => {
    const { architectureOverview } = composer.render(POPULATED_INPUTS);
    const h2Positions = ["contracts", "graph", "utils"].map((m) =>
      architectureOverview.indexOf(`## ${m}`),
    );
    expect(h2Positions[0]).toBeLessThan(h2Positions[1]!);
    expect(h2Positions[1]).toBeLessThan(h2Positions[2]!);
  });

  it("hotspots sorted by score descending (top entry has score 95)", () => {
    const { hotspots } = composer.render(POPULATED_INPUTS);
    const rows = hotspots.split("\n").filter((l) => l.startsWith("| ") && !l.startsWith("| Symbol") && !l.startsWith("| ---"));
    expect(rows[0]).toContain("parseGraph");
    expect(rows[0]).toContain("95");
  });

  it("hotspots with NaN score sorted last", () => {
    const inputs: OnboardingInputs = {
      ...POPULATED_INPUTS,
      hotspots: [
        { symbol: "normalFn", file: "src/a.ts", line: 1, score: 50 },
        { symbol: "nanFn", file: "src/b.ts", line: 2, score: NaN },
      ],
    };
    const { hotspots } = composer.render(inputs);
    const rows = hotspots.split("\n").filter((l) => l.startsWith("| ") && !l.startsWith("| Symbol") && !l.startsWith("| ---"));
    expect(rows[0]).toContain("normalFn");
    expect(rows[1]).toContain("nanFn");
    expect(rows[1]).toContain("score unavailable");
  });

  it("hotspots top 20 limit: only first 20 entries rendered when >20", () => {
    const manyHotspots = Array.from({ length: 25 }, (_, i) => ({
      symbol: `fn${String(i).padStart(2, "0")}`,
      file: "src/a.ts",
      line: i + 1,
      score: 25 - i,
    }));
    const inputs: OnboardingInputs = { ...POPULATED_INPUTS, hotspots: manyHotspots };
    const { hotspots } = composer.render(inputs);
    const rows = hotspots.split("\n").filter((l) => l.startsWith("| ") && !l.startsWith("| Symbol") && !l.startsWith("| ---"));
    expect(rows.length).toBe(20);
  });
});

