// ── writeAll — filesystem tests ────────────────────────────────────

describe("OnboardingComposer.writeAll()", () => {
  const composer = new OnboardingComposer();

  it("creates dir if it does not exist", async () => {
    const dir = join(tmp, "new-subdir", "onboarding");
    const artifacts = composer.render(POPULATED_INPUTS);
    await composer.writeAll(artifacts, dir);

    const readme = await readFile(join(dir, "README.md"), "utf-8");
    expect(readme).toContain(MARKER);
    expect(readme).toContain("# Onboarding — agent-bober graph");
  });

  it("writes all 5 files with the marker prefix", async () => {
    const artifacts = composer.render(POPULATED_INPUTS);
    await composer.writeAll(artifacts, tmp);

    for (const filename of [
      "README.md",
      "architecture-overview.md",
      "hotspots.md",
      "knowledge-gaps.md",
      "communities.md",
    ]) {
      const content = await readFile(join(tmp, filename), "utf-8");
      expect(content).toContain(MARKER);
    }
  });

  it("file absent: writes ${MARKER}\\n\\n${content}", async () => {
    const artifacts = composer.render(POPULATED_INPUTS);
    await composer.writeAll(artifacts, tmp);

    const readme = await readFile(join(tmp, "README.md"), "utf-8");
    expect(readme.startsWith(`${MARKER}\n\n`)).toBe(true);
    expect(readme).toContain("# Onboarding — agent-bober graph");
  });

  it("file with marker: preserves content above the marker, replaces below", async () => {
    // Pre-write a file with human content above the marker
    const humanContent = "# My custom heading\n\nSome human notes here.\n\n";
    await writeFile(
      join(tmp, "README.md"),
      `${humanContent}${MARKER}\n\nOLD GENERATED CONTENT`,
      "utf-8",
    );

    const artifacts = composer.render(POPULATED_INPUTS);
    await composer.writeAll(artifacts, tmp);

    const updated = await readFile(join(tmp, "README.md"), "utf-8");
    // Human content above preserved
    expect(updated.startsWith(humanContent)).toBe(true);
    // Marker present
    expect(updated).toContain(MARKER);
    // Old content replaced
    expect(updated).not.toContain("OLD GENERATED CONTENT");
    // New content present
    expect(updated).toContain("# Onboarding — agent-bober graph");
  });

  it("file without marker: throws with clear error naming the file", async () => {
    // Write a file without the generation marker
    await writeFile(join(tmp, "README.md"), "# Human-written README\n\nNo marker here.", "utf-8");

    const artifacts = composer.render(POPULATED_INPUTS);
    await expect(composer.writeAll(artifacts, tmp)).rejects.toThrow(
      "Refusing to overwrite README.md: missing generation marker.",
    );
  });

  it("throws clear error for empty dir argument", async () => {
    const artifacts = composer.render(POPULATED_INPUTS);
    await expect(composer.writeAll(artifacts, "")).rejects.toThrow(
      "writeAll: dir argument must be a non-empty path.",
    );
  });
});

// ── Performance ────────────────────────────────────────────────────

describe("OnboardingComposer.render() — performance", () => {
  it("renders 1000-element inputs in <100ms", () => {
    const composer = new OnboardingComposer();

    const bigInputs: OnboardingInputs = {
      status: { tokensaveVersion: "6.0.0-beta.1", indexedFileCount: 5000 },
      hotspots: Array.from({ length: 1000 }, (_, i) => ({
        symbol: `fn${i}`,
        file: `src/module${i % 50}.ts`,
        line: (i % 100) + 1,
        score: 1000 - i,
        reason: `Reason for function ${i}`,
      })),
      deadCode: Array.from({ length: 1000 }, (_, i) => ({
        symbol: `dead${i}`,
        file: `src/dead${i % 20}.ts`,
        line: (i % 100) + 1,
      })),
      circular: Array.from({ length: 100 }, (_, i) => ({
        cycle: [`src/a${i}.ts`, `src/b${i}.ts`, `src/a${i}.ts`],
      })),
      largest: Array.from({ length: 100 }, (_, i) => ({
        symbol: `BigClass${i}`,
        file: `src/big${i}.ts`,
        line: 1,
        loc: 500 + i,
      })),
      moduleApis: Array.from({ length: 100 }, (_, i) => ({
        module: `module-${String(i).padStart(3, "0")}`,
        community: i % 5 === 0 ? "core" : i % 5 === 1 ? "utils" : i % 5 === 2 ? "api" : i % 5 === 3 ? "internal" : "external",
        symbols: Array.from({ length: 10 }, (__, j) => ({
          name: `Symbol${i}_${j}`,
          file: `src/module${i}.ts`,
          line: j + 1,
          hasInternalCallers: j % 2 === 0,
        })),
      })),
      files: Array.from({ length: 1000 }, (_, i) => ({
        path: `src/file${i}.ts`,
        symbols: i % 10,
      })),
    };

    const start = Date.now();
    const result = composer.render(bigInputs);
    const elapsed = Date.now() - start;

    expect(elapsed).toBeLessThan(100);
    // All 5 fields populated
    expect(result.readme.length).toBeGreaterThan(0);
    expect(result.architectureOverview.length).toBeGreaterThan(0);
    expect(result.hotspots.length).toBeGreaterThan(0);
    expect(result.knowledgeGaps.length).toBeGreaterThan(0);
    expect(result.communities.length).toBeGreaterThan(0);
  });
});

